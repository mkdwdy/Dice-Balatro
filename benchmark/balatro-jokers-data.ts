/**
 * 조커 카드 검색 및 필터링 헬퍼
 */
export function getJokersByTier(tier: string): BalatroJoker[] {
  return balatroJokers.filter(j => j.tier === tier);
}

export function getJokersByRarity(rarity: string): BalatroJoker[] {
  return balatroJokers.filter(j => j.rarity === rarity);
}

export function getJokersByEffectType(effectType: string): BalatroJoker[] {
  return balatroJokers.filter(j => j.effectType === effectType);
}

export function getJokerById(id: string): BalatroJoker | undefined {
  return balatroJokers.find(j => j.id === id);
}

